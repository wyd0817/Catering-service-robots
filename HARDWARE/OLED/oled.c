//OLED的显存
//存放格式如下.
//[0]0 1 2 3 ... 127	
//[1]0 1 2 3 ... 127	
//[2]0 1 2 3 ... 127	
//[3]0 1 2 3 ... 127	
//[4]0 1 2 3 ... 127	
//[5]0 1 2 3 ... 127	
//[6]0 1 2 3 ... 127	
//[7]0 1 2 3 ... 127 			   
u8 OLED_GRAM_0[128][8],OLED_GRAM_1[128][8],OLED_GRAM_2[128][8];	 

//更新显存到LCD		 
void OLED_Refresh_Gram(u8 cs)
{
	u8 i,n;		    
	for(i=0;i<8;i++)  
	{  
		OLED_WR_Byte (0xb0+i,OLED_CMD,cs);    //设置页地址（0~7）
		OLED_WR_Byte (0x00,OLED_CMD,cs);      //设置显示位置―列低地址
		OLED_WR_Byte (0x10,OLED_CMD,cs);      //设置显示位置―列高地址   
		if(cs == 0)for(n=0;n<128;n++)			OLED_WR_Byte(OLED_GRAM_0[n][i],OLED_DATA,cs); 
		else if(cs == 1)for(n=0;n<128;n++)OLED_WR_Byte(OLED_GRAM_1[n][i],OLED_DATA,cs); 
		else if(cs == 2)for(n=0;n<128;n++)OLED_WR_Byte(OLED_GRAM_2[n][i],OLED_DATA,cs); 
	}   
}

//向SSD1306写入一个字节。
//dat:要写入的数据/命令
//cmd:数据/命令标志 0,表示命令;1,表示数据;
void OLED_WR_Byte(u8 dat,u8 cmd,u8 cs)
{	
	u8 i;			  
	if(cmd)
	  OLED_RS_Set();
	else 
	  OLED_RS_Clr();
	
	if(cs==0)				OLED_CS0_Clr();
	else if(cs==1)	OLED_CS1_Clr();
	else if(cs==2)	OLED_CS2_Clr();
	
	for(i=0;i<8;i++)
	{			  
		OLED_SCLK_Clr();
		if(dat&0x80)
		   OLED_SDIN_Set();
		else 
		   OLED_SDIN_Clr();
		OLED_SCLK_Set();
		dat<<=1;   
	}				

	if(cs==0)				OLED_CS0_Set();
	else if(cs==1)	OLED_CS1_Set();
	else if(cs==2)	OLED_CS2_Set();	
	OLED_RS_Set();   	  
} 
	  	  
//开启OLED显示    
void OLED_Display_On(u8 cs)
{
	OLED_WR_Byte(0X8D,OLED_CMD,cs);  //SET DCDC命令
	OLED_WR_Byte(0X14,OLED_CMD,cs);  //DCDC ON
	OLED_WR_Byte(0XAF,OLED_CMD,cs);  //DISPLAY ON
}
//关闭OLED显示     
void OLED_Display_Off(u8 cs)
{
	OLED_WR_Byte(0X8D,OLED_CMD,cs);  //SET DCDC命令
	OLED_WR_Byte(0X10,OLED_CMD,cs);  //DCDC OFF
	OLED_WR_Byte(0XAE,OLED_CMD,cs);  //DISPLAY OFF
}		   			 
//清屏函数,清完屏,整个屏幕是黑色的!和没点亮一样!!!	  
void OLED_Clear(u8 cs)  
{  
	u8 i,n;  
	
		if(cs == 0)			for(i=0;i<8;i++)for(n=0;n<128;n++)OLED_GRAM_0[n][i]=0X00;  
		else if(cs == 1)for(i=0;i<8;i++)for(n=0;n<128;n++)OLED_GRAM_1[n][i]=0X00;   
		else if(cs == 2)for(i=0;i<8;i++)for(n=0;n<128;n++)OLED_GRAM_2[n][i]=0X00;  
	  
	OLED_Refresh_Gram(cs);//更新显示
}
//画点 
//x:0~127
//y:0~63
//t:1 填充 0,清空				   
void OLED_DrawPoint(u8 x,u8 y,u8 t,u8 cs)
{
	u8 pos,bx,temp=0;
	if(x>127||y>63)return;//超出范围了.
	pos=7-y/8;
	bx=y%8;
	temp=1<<(7-bx);
			
	if(cs == 0)			{if(t)OLED_GRAM_0[x][pos]|=temp;else OLED_GRAM_0[x][pos]&=~temp;}	
	else if(cs == 1){if(t)OLED_GRAM_1[x][pos]|=temp;else OLED_GRAM_1[x][pos]&=~temp;}	   
	else if(cs == 2){if(t)OLED_GRAM_2[x][pos]|=temp;else OLED_GRAM_2[x][pos]&=~temp;}	
    
}
//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void OLED_Fill(u8 x1,u8 y1,u8 x2,u8 y2,u8 dot,u8 cs)  
{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot,cs);
	}													    
	OLED_Refresh_Gram(cs);//更新显示
}
//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 
//size:选择字体 16/12 
void OLED_ShowChar(u8 x,u8 y,u8 chr,u8 size,u8 mode,u8 cs)
{      			    
	u8 temp,t,t1;
	u8 y0=y;
	chr=chr-' ';//得到偏移后的值				   
    for(t=0;t<size;t++)
    {   
		if(size==12)temp=oled_asc2_1206[chr][t];  //调用1206字体
		else temp=oled_asc2_1608[chr][t];		 //调用1608字体 	                          
        for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)OLED_DrawPoint(x,y,mode,cs);
			else OLED_DrawPoint(x,y,!mode,cs);
			temp<<=1;
			y++;
			if((y-y0)==size)
			{
				y=y0;
				x++;
				break;
			}
		}  	 
    }          
}
//m^n函数
u32 oled_pow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}				  
//显示2个数字
//x,y :起点坐标	 
//len :数字的位数
//size:字体大小
//mode:模式	0,填充模式;1,叠加模式
//num:数值(0~4294967295);	 		  
void OLED_ShowNum(u8 x,u8 y,u32 num,u8 len,u8 size,u8 cs)
{         	
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(num/oled_pow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				OLED_ShowChar(x+(size/2)*t,y,' ',size,1,cs);
				continue;
			}else enshow=1; 
		 	 
		}
	 	OLED_ShowChar(x+(size/2)*t,y,temp+'0',size,1,cs); 
	}
} 
//显示字符串
//x,y:起点坐标  
//*p:字符串起始地址
//用16字体
void OLED_ShowString(u8 x,u8 y,u8 *p,u8 cs)
{
#define MAX_CHAR_POSX 122
#define MAX_CHAR_POSY 58          
    while(*p!='\0')
    {       
        if(x>MAX_CHAR_POSX){x=0;y+=16;}
        if(y>MAX_CHAR_POSY){y=x=0;OLED_Clear(cs);}
        OLED_ShowChar(x,y,*p,16,1,cs);	 
        x+=8;
        p++;
    }  
}	
//在指定位置显示一个汉字,48*32
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示  
//size:汉字为192 
void OLED_ShowChinese(u8 x,u8 y,u8 chr,u8 mode,u8 cs)
{           
	u8 temp,t,t1;
	u8 y0=y;    
	for(t=0;t<192;t++)
	{   
		temp=oled_chinese_4832[chr][t];                           
		for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)OLED_DrawPoint(x,y,mode,cs);
			else OLED_DrawPoint(x,y,!mode,cs);
			temp<<=1;
			y++;
			if((y-y0)==48)
			{
				y=y0;
				x++;
				break;
			}
		}    
	}          
} 
//显示一串汉字
//x,y:起点坐标  
//num:汉字的个数
//mode:模式	0,填充模式;1,叠加模式
//用4832字体
void OLED_ShowChinese_num(u8 x,u8 y,u8 num,u8 mode,u8 cs)
{		  
#define MAX_CHINESE_POSX 120
#define MAX_CHINESE_POSY 48 
	u8 temp_num = 0;         
	while(temp_num != num)
	{       
		if(x>MAX_CHINESE_POSX){y=x=0;OLED_Clear(cs);}
		if(y>MAX_CHINESE_POSY){y=x=0;OLED_Clear(cs);}
		OLED_ShowChinese(x,y,temp_num,mode,cs);	 
		x+=32;
		temp_num++;
	}  
}
void OLED_IO_Init(void)
{
	GPIO_InitTypeDef  GPIO_InitStructure;
	
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB|RCC_APB2Periph_GPIOC, ENABLE);	 //使能PB,C端口时钟

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10|GPIO_Pin_11|GPIO_Pin_12|GPIO_Pin_13;	 //PC10,PC11推挽输出  
 	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		 //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;//速度50MHz
 	GPIO_Init(GPIOC, &GPIO_InitStructure);	  //PC10,PC11
 	GPIO_SetBits(GPIOC,GPIO_Pin_10|GPIO_Pin_11|GPIO_Pin_12|GPIO_Pin_13);	//PC10,PC11 输出高

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12;				 //PC12 OUT推挽输出
 	GPIO_Init(GPIOC, &GPIO_InitStructure);
 	GPIO_SetBits(GPIOC,GPIO_Pin_12);						 //PC12 OUT  输出高

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_5|GPIO_Pin_4;				 //PB5,PB4 OUT推挽输出	  RST
 	GPIO_Init(GPIOB, &GPIO_InitStructure);
 	GPIO_SetBits(GPIOB,GPIO_Pin_5|GPIO_Pin_4);						 //PB5,PB4 OUT  输出高
	
	OLED_RST_Clr();
	delay_ms(100);
	OLED_RST_Set(); 
}
//初始化SSD1306					    
void OLED_Init(u8 cs)
{ 					  
	OLED_WR_Byte(0xAE,OLED_CMD,cs); //关闭显示
	OLED_WR_Byte(0xD5,OLED_CMD,cs); //设置时钟分频因子,震荡频率
	OLED_WR_Byte(80,OLED_CMD,cs);   //[3:0],分频因子;[7:4],震荡频率
	OLED_WR_Byte(0xA8,OLED_CMD,cs); //设置驱动路数
	OLED_WR_Byte(0X3F,OLED_CMD,cs); //默认0X3F(1/64) 
	OLED_WR_Byte(0xD3,OLED_CMD,cs); //设置显示偏移
	OLED_WR_Byte(0X00,OLED_CMD,cs); //默认为0

	OLED_WR_Byte(0x40,OLED_CMD,cs); //设置显示开始行 [5:0],行数.
													    
	OLED_WR_Byte(0x8D,OLED_CMD,cs); //电荷泵设置
	OLED_WR_Byte(0x14,OLED_CMD,cs); //bit2，开启/关闭
	OLED_WR_Byte(0x20,OLED_CMD,cs); //设置内存地址模式
	OLED_WR_Byte(0x02,OLED_CMD,cs); //[1:0],00，列地址模式;01，行地址模式;10,页地址模式;默认10;
	OLED_WR_Byte(0xA1,OLED_CMD,cs); //段重定义设置,bit0:0,0->0;1,0->127;
	OLED_WR_Byte(0xC0,OLED_CMD,cs); //设置COM扫描方向;bit3:0,普通模式;1,重定义模式 COM[N-1]->COM0;N:驱动路数
	OLED_WR_Byte(0xDA,OLED_CMD,cs); //设置COM硬件引脚配置
	OLED_WR_Byte(0x12,OLED_CMD,cs); //[5:4]配置
		 
	OLED_WR_Byte(0x81,OLED_CMD,cs); //对比度设置
	OLED_WR_Byte(0xEF,OLED_CMD,cs); //1~255;默认0X7F (亮度设置,越大越亮)
	OLED_WR_Byte(0xD9,OLED_CMD,cs); //设置预充电周期
	OLED_WR_Byte(0xf1,OLED_CMD,cs); //[3:0],PHASE 1;[7:4],PHASE 2;
	OLED_WR_Byte(0xDB,OLED_CMD,cs); //设置VCOMH 电压倍率
	OLED_WR_Byte(0x30,OLED_CMD,cs); //[6:4] 000,0.65*vcc;001,0.77*vcc;011,0.83*vcc;

	OLED_WR_Byte(0xA4,OLED_CMD,cs); //全局显示开启;bit0:1,开启;0,关闭;(白屏/黑屏)
	OLED_WR_Byte(0xA6,OLED_CMD,cs); //设置显示方式;bit0:1,反相显示;0,正常显示	    						   
	OLED_WR_Byte(0xAF,OLED_CMD,cs); //开启显示	 
	OLED_Clear(cs);
}  
